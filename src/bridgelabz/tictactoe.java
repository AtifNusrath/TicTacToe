package bridgelabz;

public class tictactoe {

    public static void ticTacToe() {

        char gameArray[] = new char[10];
        for (int i = 0; i < gameArray.length; i++) {

            gameArray[i] = ' ';
        }
    }

    public static void main(String[] args) {
        ticTacToe();
    }
}
